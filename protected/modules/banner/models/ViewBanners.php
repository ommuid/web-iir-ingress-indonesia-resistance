	public function attributeLabels()
	{
		return array(
			'banner_id' => Yii::t('attribute', 'Banner'),
			'clicks' => Yii::t('attribute', 'Clicks'),
			'views' => Yii::t('attribute', 'Views'),
		);
		/*
